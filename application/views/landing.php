    <h1>TheoQuotesAPI:</h1>
    <h2>An API for random words and quotes by theologians.</h2>

    `/quotes`<br />
    Returns all quotes

    <br /><br />

    `/authors`<br />
    Returns a list of all authors

    <br /><br />

    `/quotes_from/&lt;author name&gt;`<br />
    Returns all quotes from the given author

    <br /><br />

    `/quote_from/&lt;author name&gt;`<br />
    Returns a random quote from the given author

    <br /><br />

    `/quote`<br />
    Returns a random quote

    <br /><br />

    `/word`<br />
    Returns a random word

    <br /><br />

    `/words/&lt;n&gt;`<br />
    Returns n random words (n is an integer from 1 -> not 1)

